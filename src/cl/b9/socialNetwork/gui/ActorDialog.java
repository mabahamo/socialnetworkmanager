private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
    boolean valid = true;
    String label = txtName.getText().trim();
    if (label.length()==0){
        Popup.showError(this, "Nombre de actor no puede ser vac√≠o");
        valid = false;
    }
    if (!valid){
        return;
    }
    try {
        if (edit){
            node.setLabel(txtName.getText().trim());
        }
        else {
            logger.debug("Crear actor " + label + " en " + p);
            SNDirector.getInstance().createActor(actorType,label,p);
        }
    } catch(SQLException ex){
        logger.error("Error al crear el actor " + ex.getLocalizedMessage());
        Popup.showError(this, "Error al crear el actor \n" + ex.getLocalizedMessage());
        ex.printStackTrace();
    }
    this.dispose();
    
    
   
}//GEN-LAST:event_btnCreateActionPerformed

